-- (Exemplo 1) Agrupamento de dados com CASE WHEN
-- Calcule o nº de clientes que ganham abaixo de 5k, entre 5k e 10k, entre 10k e 
-- 15k e acima de 15k
WITH faixa_renda AS (
SELECT income,
CASE 
	WHEN income < 5000 then'5k'
	WHEN income >= 5000 AND income < 10000 THEN '10K'
	WHEN income >= 10000 AND income < 15000 THEN '15k'
	ELSE '+15k'
	END AS faixa_renda_grupo
	FROM sales.customers
)
SELECT faixa_renda_grupo , COUNT(*)
FROM faixa_renda
GROUP BY faixa_renda_grupo;
